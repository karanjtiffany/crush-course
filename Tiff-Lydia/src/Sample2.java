/** relational operators -works on primitives and returns boolean values (true, false)
 * <, >, <=, >=, ==, !=
 */
 
public class Sample2 {

	public static void main(String[] args)
	{
		int n1=100;
		int n2=200;
		System.out.println(n1<n2);
		System.out.println(n1>n2);
		System.out.println(n2<=n1);
		System.out.println(n1>=n2);
		
				
		

	}

}
