/*logical operators - works on boolean and returns boolean values
 * && -and operator
 * || -or operator
 * ! - not operator
 */
public class Sample3 {

	public static void main(String[] args)
	{
	
	   
	    
		int n1=100;
		int n2=200;
		System.out.println((n1<n2)&&(n1>n2)); 
		System.out.println((n1>n2)||(n1>n2)); 
	    System.out.println(!(n1>n2)); 


	

	}

}

			